/** 格式化日期为 YYYY-MM-DD（本地时区） */
function toDateStr(d: Date): string {
  const y = d.getFullYear()
  const m = String(d.getMonth() + 1).padStart(2, "0")
  const day = String(d.getDate()).padStart(2, "0")
  return `${y}-${m}-${day}`
}

/** 是否为周末 */
export function isWeekend(d: Date = new Date()): boolean {
  const wd = d.getDay()
  return wd === 0 || wd === 6
}

/** 是否为国家法定节假日 */
export function isPublicHoliday(d: Date = new Date()): boolean {
  return toDateStr(d) in H
}

/** 是否为交易日（非周末 且 非法定节假日） */
export function isTradingDay(d: Date = new Date()): boolean {
  return !isWeekend(d) && !isPublicHoliday(d)
}

/** 获取今日节假日名称，无则返回 null */
export function getTodayHolidayName(d: Date = new Date()): string | null {
  return H[toDateStr(d)] ?? null
}

/** 返回距下一个交易日的天数（0=今天就是交易日） */
export function daysUntilNextTradingDay(d: Date = new Date()): number {
  const cursor = new Date(d)
  let count = 0
  while (!isTradingDay(cursor)) {
    cursor.setDate(cursor.getDate() + 1)
    count++
    if (count > 30) break // 防无限循环
  }
  return count
}
